/* Penjumlahan 2 paramater
Jumlah angka dari parameter yang disediakan */

function solution1(a, b) {
    return console.log(a + b)
}

solution1(2, 2)

/* pengurangan antara 2 parameter
lakukan penguragan antara parameter a dan b */

function solution2(a, b) {
    return console.log(a - b)
}

solution2(2, 2)

